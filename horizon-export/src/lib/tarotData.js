export const getRandomCards = (count) => {
  const shuffled = [...tarotCards].sort(() => 0.5 - Math.random());
  return shuffled.slice(0, count).map((card, index) => ({
    ...card,
    reversed: Math.random() > 0.7, // 30% chance of reversal
    position: index,
  }));
};

